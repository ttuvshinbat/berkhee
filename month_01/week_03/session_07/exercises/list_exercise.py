#Dasgal 2 

# 5 тоо агуулсан жагсаалт
numbers = [10, 20, 30, 40, 50]
print(numbers)

# 3 дахь элементийг өөр тоогоор солино (жишээ нь 99)
numbers[2] = 99
print("3 дахь элемент өөрчлөгдсөний дараа:", numbers)

# Жагсаалтын төгсгөлд шинэ тоо нэмнэ (жишээ нь 60)
numbers.append(60)
print("Төгсгөлд тоо нэмсний дараа:", numbers)

# Жагсаалтын эхэнд шинэ тоо оруулна (жишээ нь 5)
numbers.insert(0, 5)
print("Эхэнд тоо нэмсний дараа:", numbers)

# Жагсаалтаас нэг элементийг устгана (жишээ нь 40-г устгах)
numbers.remove(40)
print("Элемент устгасны дараа:", numbers)

# Dasgal 3 
# Дурын тоонуудыг агуулсан жагсаалт
numbers = [42, 17, 8, 99, 23]

# Жагсаалтыг өсөх эрэмбээр эрэмбэлнэ
numbers.sort()
print("Өсөх эрэмбээр:", numbers)

# Жагсаалтыг буурах эрэмбээр эрэмбэлнэ
numbers.sort(reverse=True)
print("Буурах эрэмбээр:", numbers)

# Жагсаалтын хуулбарыг үүсгэнэ
numbers_copy = numbers.copy()
print("Жагсаалтын хуулбар:", numbers_copy)

# Жагсаалтын уртыг хэвлэнэ
print("Жагсаалтын урт:", len(numbers))

