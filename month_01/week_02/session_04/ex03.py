decimal = 12312997
print(f"decimal number is : {decimal}")
binary = bin(decimal)
print(f"binary number is : {binary}")
octal = oct(decimal)
print(f"octal number is : {octal}")
hexadecimal = hex(decimal)
print(f"hexadecimal number is : {hexadecimal}")

